    private void jRCAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRCAddButtonActionPerformed
        if (db.hasHold(Integer.parseInt(jTextFieldIRCStudentID.getText()))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "Student Has Hold");
        } else if (!db.checkPrerequisite(Integer.parseInt(jTextFieldIRCStudentID.getText()),
                db.getCourseID(jRCCourseComboBox.getSelectedItem().toString()))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "This Course Has Prerequisite That Student Needs To Finish");
        }
        else if (!db.alreadyEnrolled(db.getCourseID(jRCCourseComboBox.getSelectedItem().toString()),
                Integer.parseInt(jTextFieldIRCStudentID.getText()))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "This Course Is Already Registered");
        } else if (!db.canRegister(db.getCourseID(jRCCourseComboBox.getSelectedItem().toString()),
                Integer.parseInt(jTextFieldIRCStudentID.getText()))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "Student Took This Course Before [ Grade > D+ ]");
        } else if (!db.canAttend(db.getSectionID(jRCSectionComboBox.getSelectedItem().toString(), db.getCourseID(jRCCourseComboBox.getSelectedItem().toString())))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "This Section is Full");
        } else if (!db.hasNoConflict(jRCSectionComboBox.getSelectedItem().toString(), Integer.parseInt(jTextFieldIRCStudentID.getText()))) {
            JOptionPane.showMessageDialog(jRegisterCoursePanel, "Time Conflict With Other Section");
        } else {
            db.addStudentRecord(db.getCourseID(jRCCourseComboBox.getSelectedItem().toString()), Integer.parseInt(jTextFieldIRCStudentID.getText()));
            db.addEnrolls(db.getSectionID(jRCSectionComboBox.getSelectedItem().toString(), db.getCourseID(jRCCourseComboBox.getSelectedItem().toString())), Integer.parseInt(jTextFieldIRCStudentID.getText()));
            db.decreaseAvailable(jRCSectionComboBox.getSelectedItem().toString(), db.getCourseID(jRCCourseComboBox.getSelectedItem().toString()));
            jTextFieldIRCStudentID.setText(null);
            jRCSemesterComboBox.setSelectedIndex(-1);
            jRCCourseComboBox.setSelectedIndex(-1);
            jRCSectionComboBox.setSelectedIndex(-1);
        }

    }//GEN-LAST:event_jRCAddButtonActionPerformed

