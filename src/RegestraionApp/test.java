    private void jPRHoldMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPRHoldMenuItemActionPerformed
        DefaultTableModel model = (DefaultTableModel) jPRHoldTable.getModel();
        model.setRowCount(0);
        ArrayList<String> studentID = db.getStudentIDList();
        Object rawData[] = new Object[3];
        for (int i = 0; i < studentID.size(); i++) {
            int intStudentID = Integer.parseInt(studentID.get(i));
            rawData[0] = studentID.get(i);
            rawData[1] = db.getStudentName(intStudentID);
            rawData[2] = db.getStudentHoldStatus(intStudentID);
            model.addRow(rawData);
        }
        TableRowSorter sorter = new TableRowSorter(model);
        jPRHoldTable.setRowSorter(sorter);
        jPanel.setVisible(true);
        jPanel.removeAll();
        jPanel.add(jPutHoldPanel1);
        jPanel.repaint();
        jPanel.revalidate();
    }//GEN-LAST:event_jPRHoldMenuItemActionPerformed

    private void jPutRemoveHoldButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPutRemoveHoldButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) jPRHoldTable.getModel();
        db.putHold(Integer.parseInt(model.getValueAt(jPRHoldTable.getRowSorter().convertRowIndexToModel(jPRHoldTable.getSelectedRow()), 0).toString()),
                model.getValueAt(jPRHoldTable.getRowSorter().convertRowIndexToModel(jPRHoldTable.getSelectedRow()), 2).toString());
        if (model.getValueAt(jPRHoldTable.getRowSorter().convertRowIndexToModel(jPRHoldTable.getSelectedRow()), 2).toString().equals("T")) {
            model.setValueAt("F", jPRHoldTable.getRowSorter().convertRowIndexToModel(jPRHoldTable.getSelectedRow()), 2);
        } else {
            model.setValueAt("T", jPRHoldTable.getRowSorter().convertRowIndexToModel(jPRHoldTable.getSelectedRow()), 2);
        }
    }//GEN-LAST:event_jPutRemoveHoldButtonActionPerformed

